
package proyectos_poo.poo_basico.herencia_abstraccion.SportifyTech.enums;

/**
 * Enumeración que define los niveles de habilidad de los competidores.
 * <p>
 * Esta enumeración representa los tres niveles de habilidad disponibles
 * en el sistema deportivo SportifyTech para clasificar a los competidores.
 * </p>
 *
 * @author Caterine Salinas Bolaños
 * @version 1.0
 */
public enum Nivel {
    /**
     * Nivel principiante para competidores que inician en el deporte.
     */
    PRINCIPIANTE,
    
    /**
     * Nivel intermedio para competidores con experiencia moderada.
     */
    INTERMEDIO,
    
    /**
     * Nivel avanzado para competidores con alta experiencia y habilidad.
     */
    AVANZADO;
}
